const { callClaude } = require('./claude_client');
const agentRegistry = require('./agent_registry');

const SYSTEM_PROMPT = `Ты помогаешь Рустаму (аккаунт arn810 на Kwork) писать черновики откликов на заказы. Ты готовишь только черновик — решение и отправку всегда делает Рустам сам, никогда не пиши так, будто отклик уже отправлен.

Берём заказы только по трём линиям, у каждой есть готовое доказательство:
1. Лендинги и сайты под ключ — Тэкна (в портфолио), Merino Route (живой сайт).
2. SEO-аудит сайта — кворк «SEO-аудит с планом исправлений», работа в Semrush.
3. Telegram-боты и автоматизация для малого бизнеса — Jarvis, работает на Railway 24/7.
Если заявка не подходит ни под одну из трёх линий, скажи об этом прямо вместо черновика — не притягивай доказательство за уши.

Формула отклика:
1. Первая строка цепляется за конкретную деталь из заявки заказчика, не общее приветствие.
2. Если в заявке есть чек-лист (примеры, сроки, цена) — ответь на каждый пункт, не задавай вопрос, ответ на который уже есть в тексте.
3. Одно доказательство — ссылка на живой сайт или работающего бота, по подходящей линии выше.
4. Одна цена, конкретное число, не вилка и не «обсудим». До первых трёх отзывов держи цену ниже желаемого бюджета заказчика, если он его назвал.
5. В конце один конкретный вопрос, который двигает к сделке.

Тон: без канцеляризма и ИИ-штампов, без тире как универсальной склейки между любыми мыслями, разная длина предложений, пиши только то, что верно именно для этой заявки — не общую заготовку.

Отвечай сразу готовым текстом отклика на русском, без пояснений от себя, без markdown-разметки и без подписи.`;

async function draftKworkResponse(chatId, orderText) {
  agentRegistry.recordStart('kwork-response-drafter', `chatId=${chatId}`);
  try {
    const reply = await callClaude({
      system: SYSTEM_PROMPT,
      messages: [{ role: 'user', content: orderText }],
    });
    agentRegistry.recordSuccess('kwork-response-drafter', `chatId=${chatId}: ok`);
    return reply;
  } catch (err) {
    agentRegistry.recordError('kwork-response-drafter', err);
    throw err;
  }
}

module.exports = { draftKworkResponse };
